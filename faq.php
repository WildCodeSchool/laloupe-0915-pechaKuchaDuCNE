					    <p>
						Même si vous n’êtes pas sélectionnés pour participer à la soirée Pecha Kucha, ou même si vous êtes sélectionnés,
						mais pas lauréats, vous aurez:
							<ul>
								<li>reçu un retour bref mais précis sur votre projet ;</li>
								<li>fait circuler votre projet;</li>
								<li>appris un nouvel exercice.</li>
							</ul>
						Certes, les normaliens ne sont pas les plus mal armés pour affronter le monde de l’innovation mais,
						en même temps, ils peuvent manquer de confiance en eux. Ils peuvent aussi manquer d’un nécessaire sens de
						la communication. Ce concours a pour fin de les aider à essayer, tenter, expérimenter.
					 	</p>
					  


				  <h3>Comment participer ? </h3>
					  
					    <p>
					    Pour soumettre votre projet, il suffit d’envoyer, dans un résumé de votre projet, d’une page, au format
					    PDF, expliquant en moins de 1000 signes votre projet. Avec, sur ce document, le nom de votre projet,
					    votre logo (s’il existe!), et vos coordonnées. Attention : il faut faire cela avant <strong>le 25 août 2015, à
					    minuit</strong>. N’oubliez pas, dans votre message, d’indiquer qui, dans votre équipe, est membre de la
					    “communauté normalienne”. Fin août, vous saurez si vous êtes sélectionné pour participer au premier
					    Pecha Kucha du CNE, qui aura lieu le jeudi 8 octobre.
					    </p>
					  


				  <h3>A quoi ressemblera la soirée Pecha Kucha pour les finalistes ?</h3>
					  
					    <p>
					    Lors d’un dîner en septembre 2015, devant les dizaines de membres du CNE et un jury d’experts, les
					    finalistes pourront présenter leur projet sous la forme d’un pecha kucha: 20 images, affichées pendant
					    20 secondes au maximum chacune. Soit 6 minutes et 40 secondes, pour convaincre, et montrer que ce projet
					    doit être soutenu. Ensuite, l’assemblée vote et détermine son lauréat. De leurs côtés, les experts, qui
					    connaissent le résultat du vote de l’assemblée, se retirent pour délibrérer et choisir leur lauréat. Le
					    jury peut choisir le même projet que l’assemblée ou un autre projet. Le ou les lauréat(s) se voient
					    désigner deux mentors, qui s’engagent à les suivre pendant un an.
					    </p>
					  


